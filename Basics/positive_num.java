package Basics;

public class positive_num {
    public static void main(String[] args) {
        int num = -1;
        if (num > 0) {
            System.out.println("The number is positive.");
        } else {
            System.out.println("The number is not positive.");
        }
        // This is a simple program to check if a number is positive
        // or not. It uses an if-else statement to determine the result.
    }
}